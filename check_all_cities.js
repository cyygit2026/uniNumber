(async () => {
    console.log('=== 城市级号段归属地检查 ===\n');
    let totalErrors = 0;
    
    for (const province of Object.keys(provinceSegments)) {
        const segments = provinceSegments[province];
        const hasCitySegments = segments && typeof segments === 'object' && !Array.isArray(segments) && 
            Object.keys(segments).some(k => !['中国移动', '中国联通', '中国电信'].includes(k));
        
        if (!hasCitySegments) continue;
        
        for (const city of Object.keys(segments)) {
            if (['中国移动', '中国联通', '中国电信'].includes(city)) continue;
            
            for (const operator of ['中国移动', '中国联通', '中国电信']) {
                const opSegments = segments[city][operator];
                if (!opSegments || opSegments.length === 0) continue;
                
                for (const seg of opSegments.slice(0, 3)) {
                    const phone = seg + '0000';
                    const loc = await getLoc(phone);
                    await new Promise(r => setTimeout(r, 30));
                    
                    if (!loc) continue;
                    
                    const cityName = city.replace('市', '');
                    if (!loc.city.includes(cityName)) {
                        console.log(`✗ ${province} ${city} ${operator} ${seg} -> ${loc.city}`);
                        totalErrors++;
                    }
                }
            }
        }
    }
    
    console.log(`\n=== 检查完成 ===`);
    console.log(`发现 ${totalErrors} 个城市级归属地不一致问题`);
})();
