// 验证并修正号段数据
async function validateSegments() {
    console.log('=== 验证并修正号段数据 ===\n');
    
    for(const province of Object.keys(phoneSegments)) {
        const segments = phoneSegments[province];
        const hasCitySegments = segments && typeof segments === 'object' && !Array.isArray(segments) && 
            Object.keys(segments).some(k => !['中国移动', '中国联通', '中国电信'].includes(k));
        
        if (hasCitySegments) {
            // 城市级数据
            for(const city of Object.keys(segments)) {
                if (['中国移动', '中国联通', '中国电信'].includes(city)) continue;
                
                for(const operator of ['中国移动', '中国联通', '中国电信']) {
                    const opSegments = segments[city][operator];
                    if (!opSegments || opSegments.length === 0) continue;
                    
                    const validSegments = [];
                    for(const seg of opSegments) {
                        // 验证号段长度
                        if (seg.length < 7) {
                            // 5位或6位号段，需要扩展
                            if (seg.length === 5) {
                                // 扩展为6位号段并验证
                                for(let i=0; i<=9; i++) {
                                    const fullSeg = seg + i;
                                    const loc = await getLoc(fullSeg + '0000');
                                    if (loc && loc.city && loc.city.includes(city.replace('市', ''))) {
                                        validSegments.push(fullSeg);
                                    }
                                    await new Promise(r => setTimeout(r, 20));
                                }
                            } else if (seg.length === 6) {
                                const loc = await getLoc(seg + '0000');
                                if (loc && loc.city && loc.city.includes(city.replace('市', ''))) {
                                    validSegments.push(seg);
                                }
                                await new Promise(r => setTimeout(r, 20));
                            }
                        } else {
                            const loc = await getLoc(seg + '0000');
                            if (loc && loc.city && loc.city.includes(city.replace('市', ''))) {
                                validSegments.push(seg);
                            }
                            await new Promise(r => setTimeout(r, 20));
                        }
                    }
                    
                    // 更新为验证后的号段
                    segments[city][operator] = [...new Set(validSegments)];
                }
            }
        } else {
            // 省级数据
            for(const operator of ['中国移动', '中国联通', '中国电信']) {
                const opSegments = segments[operator];
                if (!opSegments || opSegments.length === 0) continue;
                
                const validSegments = [];
                for(const seg of opSegments) {
                    const loc = await getLoc(seg + '0000');
                    if (loc && loc.province && loc.province.includes(province.replace('省', '').replace('自治区', '').replace('市', ''))) {
                        validSegments.push(seg);
                    }
                    await new Promise(r => setTimeout(r, 20));
                }
                
                segments[operator] = [...new Set(validSegments)];
            }
        }
    }
    
    console.log('验证完成！');
    console.log(JSON.stringify(phoneSegments, null, 2));
}

